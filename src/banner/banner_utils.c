int	ft_sleep_exec(char *time_str)
{
	pid_t	pid;
	char	*args[3];

	args[0] = "/bin/sleep";
	args[1] = time_str;
	args[2] = NULL;
	pid = fork();
	if (pid < 0)
		return (-1);
	if (pid == 0)
	{
		execve("/bin/sleep", args, NULL);
		exit(1);
	}
	waitpid(pid, NULL, 0);
	return (0);
}
